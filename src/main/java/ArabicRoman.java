public class ArabicRoman {

    private Integer arabicValue;
    private String romanValue;

    public ArabicRoman(Integer arabicValue, String romanValue) {
        this.arabicValue = arabicValue;
        this.romanValue = romanValue;
    }

    public Integer getArabicValue() {
        return arabicValue;
    }

    public String getRomanValue() {
        return romanValue;
    }
}
